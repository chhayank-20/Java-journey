public class Autoboxing {
    public static void main(String args[]){
        // Autoboxing - to store int value to Integer 
        int num=8;
        Integer num1 =num;
        System.out.println(num1);

        
        Integer num2=4;
        System.out.println(num2);

        // Autoboxing - to store int value to Integer 
        int num3= num1;
        System.out.println(num3);
        
    }
}
